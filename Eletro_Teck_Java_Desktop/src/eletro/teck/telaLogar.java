    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String cpf, senha;

            cpf = jTextField1.getText();
            senha = jTextField2.getText();

            Funcionario objfuncionario = new Funcionario();
            objfuncionario.setCpf(cpf);
            objfuncionario.setSenha(senha);
            
            FuncionarioDAO objfuncionariodao = new FuncionarioDAO();
            ResultSet rsusuariodao = objfuncionariodao.autenticacaoLogin(objfuncionario);
            
            if (rsusuariodao.next()) {
                
                telaMenu menu = new telaMenu();
                menu.show();
                setVisible(true);
                dispose();
                
            }else if(jTextField1.getText().equals("") || jTextField2.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Preencha todos os campos");

            }else{
                JOptionPane.showMessageDialog(null, "CPF ou senha inv√°lidos");
            }
            
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, erro, "ERROR", JOptionPane.ERROR_MESSAGE);
        }  
    }//GEN-LAST:event_jButton1ActionPerformed

