    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int id;
        try {
            id = Integer.parseInt(jLabel2.getText());
        } catch (NumberFormatException e) {
            // Lidar com a exceção, por exemplo, mostrar uma mensagem de erro.
            JOptionPane.showMessageDialog(null, "ID inválido", "Erro", JOptionPane.ERROR_MESSAGE);
            return; // ou qualquer outra lógica que faça sentido no seu caso
        }
        
        String nome = jTextField1.getText();
        String telefone = jTextField2.getText();
        String endereco = jTextField3.getText();
        String nomeTecnico = jComboBox1.getSelectedItem().toString();
        String dia = jTextField4.getText();
        String escolha = jComboBox2.getSelectedItem().toString();
        String pagamento = jComboBox3.getSelectedItem().toString();
        String descricao = jTextField5.getText();
        
        if (nome.equals("") || telefone.equals("") || endereco.equals("") || nomeTecnico.equals("") || dia.equals("") || escolha.equals("") || pagamento.equals("") || descricao.equals("")) {
            
          JOptionPane.showMessageDialog(null,
                    "Por Favor!! Preencha todos os campos!", "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            
            Agendamento agendamento = new Agendamento();
            
            agendamento.setIdagendamento(id);
            agendamento.setNome(nome);
            agendamento.setTelefone(telefone);
            agendamento.setEndereco(endereco);
            agendamento.setNomeTecnico(nomeTecnico);
            agendamento.setDia(dia);
            agendamento.setEscolha(escolha);
            agendamento.setPagamento(pagamento);
            agendamento.setDescricao(descricao);
            
            AgendamentoDAO agendamentocontrol = new AgendamentoDAO();
            
            agendamentocontrol.atualizar(agendamento);
            
            telaOS1 telaOS1 = new telaOS1();
            telaOS1.show();
            setVisible(true);
            dispose(); 
        }        
    }//GEN-LAST:event_jButton1ActionPerformed

